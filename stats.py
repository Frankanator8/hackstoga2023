class Stats:
    def __init__(self, hp, defaultHP, atk, defaultAtk, defense, defaultDefense, speed, defaultSpeed):
        self.hp = hp
        self.defaultHP = defaultHP
        self.atk = atk
        self.defaultAtk = defaultAtk
        self.defense = defense
        self.defaultDefense = defaultDefense
        self.speed = speed
        self.defaultSpeed = defaultSpeed
